namespace WumpusCore.Battle
{
    /// <summary>
    /// A portion of the keyboard that may be accessed by a battle.
    /// </summary>
    public enum InputZone
    {
        /// <summary>
        /// Number keys
        /// </summary>
        Numbers,
        /// <summary>
        /// All letter keys
        /// </summary>
        Letters,
        /// <summary>
        /// The up, down, left, and right arrows
        /// </summary>
        Arrows,
        /// <summary>
        /// Tab, Shift, Ctrl, Alt, Backspace, Enter
        /// </summary>
        Controls
    }
}